package ex02;

public class hw_2 {
    public static void main(String[] args) {
        // 정수형 변수 선언
        int number = 10;
        System.out.println(number); // 출력 결과: 10

        // 실수형 변수 선언
        double doubleNumber = 3.14;
        System.out.println(doubleNumber); // 출력 결과: 3.14

        // 문자열 변수 선언
        String str = "Hello, World!";
        System.out.println(str); // 출력 결과: Hello, World!

        // 논리형 변수 선언
        boolean isTrue = true;
        System.out.println(isTrue); // 출력 결과: true
    }
}
